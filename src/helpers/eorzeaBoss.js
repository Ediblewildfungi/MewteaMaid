/**
 * 获取BOSS数据
 * @param bossNickName BOSS名称
 */

// getEorzeaBoss

const getEorzeaBoss = (bossNickName) => {

    let boss = {
        ok: false,
        bossCnServer: 5,
        difficulty:101
    }

    // 29-伊甸园 28-5.0蛮神 30-巴哈/究极 31-亚历山大

    const zoneId = [23, 29, 28, 30, 31, 32]

    if (bossNickName) {

        for (let i = 0; i < zoneId.length; i++) {

            for (let x = 0; x < eorzeaBoss.length; x++) {

                if (eorzeaBoss[x].id == zoneId[i]) {

                    for (let y = 0; y < eorzeaBoss[x].encounters.length; y++) {
                        for (let z = 0; z < eorzeaBoss[x].encounters[y].nickname.length; z++) {

                            if (eorzeaBoss[x].encounters[y].nickname[z] == bossNickName) {

                                boss.ok = true
                                boss.zone = eorzeaBoss[x].id
                                boss.id = eorzeaBoss[x].encounters[y].id
                                boss.name = eorzeaBoss[x].encounters[y].name
                                boss.cnName = eorzeaBoss[x].encounters[y].cnName

                                if (eorzeaBoss[x].encounters[y].bossCnServer) {

                                    boss.bossCnServer = eorzeaBoss[x].encounters[y].bossCnServer

                                }
                                if (eorzeaBoss[x].difficulty) {
                                    boss.difficulty = eorzeaBoss[x].difficulty
                                }
                                // return 0;
                                break;

                            }
                        }
                    }
                }
            }

        }

    }

    return boss

}


